#include "libft.h"

void	*ft_memset(void *str, int c, size_t n)
{
	unsigned int	i;
	unsigned char	*ch;

	i = 0;
	ch = (unsigned char *)str;
	while (i < n)
	{
		ch[i++] = (unsigned char)c;
	}
	str = (void *)ch;
	return (str);
}
