#include "libft.h"

void	ft_lstadd_back(t_list **alst, t_list *new)
{
	t_list	*l;

	l = *alst;
	if (l)
	{
		while (l->next)
			l = l->next;
		l->next = new;
		new->next = NULL;
	}
	else
		*alst = new;
}
